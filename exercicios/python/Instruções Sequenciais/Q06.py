# Exercício 6 — Divisão inteira de dois inteiros
def main():
    a = int(input("Informe o primeiro número inteiro: "))
    b = int(input("Informe o segundo número inteiro: "))
    resultado = a // b
    print(f"Resultado da divisão inteira ({a} // {b}): {resultado}")

if __name__ == "__main__":
    main()